



/**
 * 弹出提示框
 *
 * @param content 内容
 * @param icon 0:警告，1:正确，2:错误，3:问号，4:锁定，5:苦脸，6:笑脸
 */
function showAlert(content, icon) {
    if (!icon) {
        icon = 0;
    }
    return layer.alert(content, {icon: icon});
}
